def test_sqlite_enum_constraints_reject_invalid_raw_values(
    db_session,
    statement,
    parameters,
) -> None:
    now = datetime.now(UTC)
    db_session.add(
        Task(
            id="task-valid-for-event",
            type=TaskType.ANALYSIS,
            status=TaskStatus.QUEUED,
            client_id_hash="client",
            ip_hash="ip",
        )
    )
    db_session.commit()

    with pytest.raises(IntegrityError):
        db_session.execute(
            text(statement),
            parameters
            | {
                "now": now.replace(tzinfo=None).isoformat(),
                "expires_at": (now + timedelta(hours=1)).replace(tzinfo=None).isoformat(),
            },
        )


def test_application_session_dependency_reuses_configured_engine(tmp_path) -> None:
    engine = configure_database(f"sqlite:///{tmp_path / 'application.db'}")
    first_dependency = get_db_session()
    second_dependency = get_db_session()
    first_session = next(first_dependency)
    second_session = next(second_dependency)
    try:
        assert first_session.bind is engine
        assert second_session.bind is engine
    finally:
        first_dependency.close()
        second_dependency.close()
        configure_database(Settings().database_url)


def test_initial_migration_creates_expected_tables(monkeypatch, tmp_path) -> None:
    api_root = Path(__file__).resolve().parents[2]
    database_url = f"sqlite:///{tmp_path / 'migration.db'}"
    monkeypatch.setenv("STARUN_DATABASE_URL", database_url)

    alembic_config = Config(api_root / "alembic.ini")
    command.upgrade(alembic_config, "head")

    engine = create_engine(database_url)
    try:
        inspector = inspect(engine)
        assert {
            "alembic_version",
            "daily_usage",
            "task_events",
            "tasks",
            "uploads",
        } == set(inspector.get_table_names())
        assert {index["name"] for index in inspector.get_indexes("uploads")} == {
            "ix_uploads_status_expires_at"
        }
        assert {index["name"] for index in inspector.get_indexes("tasks")} == {
            "ix_tasks_expires_at",
            "ix_tasks_status_created_at",
        }
        assert inspector.get_indexes("task_events") == []
        assert {
            constraint["name"]
            for constraint in inspector.get_check_constraints("uploads")
        } == {"uploadstatus_enum"}
        assert {
            constraint["name"]
            for constraint in inspector.get_check_constraints("tasks")
        } == {
            "processingstyle_enum",
            "taskstatus_enum",
            "tasktype_enum",
        }
        assert {
            constraint["name"]
            for constraint in inspector.get_check_constraints("task_events")
        } == {"eventlevel_enum"}
        assert {
            constraint["name"]
            for constraint in inspector.get_unique_constraints("task_events")
        } == {"uq_task_event_sequence"}
        assert {
            constraint["name"]
            for constraint in inspector.get_unique_constraints("daily_usage")
        } == {"uq_daily_usage_client"}
        assert {
            foreign_key["referred_table"]
            for foreign_key in inspector.get_foreign_keys("tasks")
        } == {
            "tasks",
            "uploads",
        }
        event_foreign_key = inspector.get_foreign_keys("task_events")[0]
        assert event_foreign_key["referred_table"] == "tasks"
        assert event_foreign_key["options"] == {"ondelete": "CASCADE"}
    finally:
        engine.dispose()


def test_alembic_detects_server_default_drift(monkeypatch, tmp_path) -> None:
    api_root = Path(__file__).resolve().parents[2]
    database_url = f"sqlite:///{tmp_path / 'drift.db'}"
    monkeypatch.setenv("STARUN_DATABASE_URL", database_url)
    alembic_config = Config(api_root / "alembic.ini")
    command.upgrade(alembic_config, "head")
    command.check(alembic_config)

    progress_column = Task.__table__.c.progress
    original_default = progress_column.server_default
    progress_column.server_default = DefaultClause(text("99"))
    try:
        with pytest.raises(AutogenerateDiffsDetected):
            command.check(alembic_config)
    finally:
        progress_column.server_default = original_default


