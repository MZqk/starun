def test_enum_values_persist_as_lowercase_contract_strings(db_session) -> None:
    upload = Upload(
        id="upload-enums",
        client_id_hash="client",
        ip_hash="ip",
        original_file_name="m42.fits",
        stored_path="/tmp/m42.fits",
        size_bytes=256,
        status=UploadStatus.VALIDATING,
        expires_at=datetime.now(UTC) + timedelta(hours=1),
    )
    task = Task(
        id="task-enums",
        type=TaskType.PROCESSING,
        status=TaskStatus.RUNNING,
        style=ProcessingStyle.ARTISTIC,
        client_id_hash="client",
        ip_hash="ip",
        upload=upload,
    )
    task.events.append(
        TaskEvent(
            sequence=1,
            level=EventLevel.ERROR,
            event_type="failed",
        )
    )
    db_session.add(task)
    db_session.commit()

    assert db_session.execute(
        text("SELECT status FROM uploads WHERE id = 'upload-enums'")
    ).scalar_one() == "validating"
    assert db_session.execute(
        text("SELECT type || ':' || status || ':' || style FROM tasks WHERE id = 'task-enums'")
    ).scalar_one() == "processing:running:artistic"
    assert db_session.execute(
        text("SELECT level FROM task_events WHERE task_id = 'task-enums'")
    ).scalar_one() == "error"
    assert task.events[0].payload == {}


def test_datetime_values_round_trip_as_aware_utc(db_session) -> None:
    now = datetime.now(UTC)
    offset_time = now.astimezone(timezone(timedelta(hours=8)))
    upload = Upload(
        id="upload-datetime",
        client_id_hash="client",
        ip_hash="ip",
        original_file_name="m51.fits",
        stored_path="/tmp/m51.fits",
        size_bytes=512,
        status=UploadStatus.READY,
        created_at=offset_time,
        expires_at=offset_time + timedelta(hours=1),
        claimed_at=offset_time + timedelta(minutes=1),
    )
    task = Task(
        id="task-datetime",
        type=TaskType.ANALYSIS,
        status=TaskStatus.RUNNING,
        client_id_hash="client",
        ip_hash="ip",
        upload=upload,
        cancel_requested_at=offset_time + timedelta(minutes=2),
        delete_requested_at=offset_time + timedelta(minutes=2),
        cleanup_pending=True,
        cleanup_error="diagnostic_id=cleanup-test",
        cleanup_plan={"task_dir": True, "source_path": True},
        created_at=offset_time,
        started_at=offset_time + timedelta(minutes=3),
        finished_at=offset_time + timedelta(minutes=4),
        expires_at=offset_time + timedelta(hours=2),
    )
    task.events.append(
        TaskEvent(
            sequence=1,
            level=EventLevel.INFO,
            event_type="started",
            created_at=offset_time + timedelta(minutes=3),
        )
    )
    db_session.add(task)
    db_session.commit()
    db_session.expire_all()

    reloaded = db_session.get(Task, task.id)
    assert reloaded is not None
    assert reloaded.created_at.tzinfo is UTC
    assert reloaded.created_at == now
    assert reloaded.delete_requested_at == offset_time + timedelta(minutes=2)
    assert reloaded.cleanup_pending is True
    assert reloaded.cleanup_error == "diagnostic_id=cleanup-test"
    assert reloaded.cleanup_plan == {"task_dir": True, "source_path": True}
    stored_created_at = db_session.execute(
        text("SELECT created_at FROM tasks WHERE id = 'task-datetime'")
    ).scalar_one()
    assert datetime.fromisoformat(stored_created_at) == now.replace(tzinfo=None)
    assert reloaded.upload is not None
    assert reloaded.upload.expires_at.tzinfo is UTC
    assert reloaded.events[0].created_at.tzinfo is UTC


